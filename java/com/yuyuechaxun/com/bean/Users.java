package com.yuyuechaxun.com.bean;


public class Users {
    private Integer usid;
    private String usname;
    private String uspass;
    private String usphone;
    private String usemail;
    // 身份
    private String usden;
    // 重置密码
    private String uspass2;

    public Integer getUsid() {
        return usid;
    }

    public void setUsid(Integer usid) {
        this.usid = usid;
    }

    public String getUsname() {
        return usname;
    }

    public void setUsname(String usname) {
        this.usname = usname;
    }

    public String getUspass() {
        return uspass;
    }

    public void setUspass(String uspass) {
        this.uspass = uspass;
    }

    public String getUsphone() {
        return usphone;
    }

    public void setUsphone(String usphone) {
        this.usphone = usphone;
    }

    public String getUsemail() {
        return usemail;
    }

    public void setUsemail(String usemail) {
        this.usemail = usemail;
    }

    public String getUsden() {
        return usden;
    }

    public void setUsden(String usden) {
        this.usden = usden;
    }

    public String getUspass2() {
        return uspass2;
    }

    public void setUspass2(String uspass2) {
        this.uspass2 = uspass2;
    }
}
